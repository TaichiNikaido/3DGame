//=============================================================================
// 攻撃力設定関数
//=============================================================================
void CWeapon::SetAttack(int nAttack)
{
	CPlayer * pPlayer = CManager::GetPlayer();

	//攻撃力の設定
	m_nAttack = nAttack;
	//プレイヤーの攻撃力の設定
	pPlayer->SetAttack(m_nAttack);
}

